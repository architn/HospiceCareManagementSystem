package Business.Volunteers;

/**
 *
 * @author architnigam
 */
public class Volunteer {
    public String volunteerID;
    public String volunteerName;
    public String volunteerAddress;
    public String volunteerEmailID;
    public String volunteerContactNo;

    public String getVolunteerID() {
        return volunteerID;
    }

    public void setVolunteerID(String volunteerID) {
        this.volunteerID = volunteerID;
    }

    public String getVolunteerName() {
        return volunteerName;
    }

    public void setVolunteerName(String volunteerName) {
        this.volunteerName = volunteerName;
    }

    public String getVolunteerAddress() {
        return volunteerAddress;
    }

    public void setVolunteerAddress(String volunteerAddress) {
        this.volunteerAddress = volunteerAddress;
    }

    public String getVolunteerEmailID() {
        return volunteerEmailID;
    }

    public void setVolunteerEmailID(String volunteerEmailID) {
        this.volunteerEmailID = volunteerEmailID;
    }

    public String getVolunteerContactNo() {
        return volunteerContactNo;
    }

    public void setVolunteerContactNo(String volunteerContactNo) {
        this.volunteerContactNo = volunteerContactNo;
    }
}
