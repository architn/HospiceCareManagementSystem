    private void btnAlertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlertActionPerformed
        // TODO add your handling code here:
        String message = "";
        patient = system.getPatientDirectory().findPatientByEmailID(account.getUsername(), 
                system.getPatientDirectory().getPatientList());
        reportingProvider = patient.getRegisteredProvider();
        String alertReason = ddSymptom.getSelectedItem().toString();
        int alertLevel = 0;
        if(alertReason.equalsIgnoreCase("Flu-like symptoms"))
        {
            alertLevel = 0;
        }
        else if(alertReason.equalsIgnoreCase("Dizziness"))
        {
            alertLevel = 1;    
         }
        else if(alertReason.equalsIgnoreCase("Chest pain") || alertReason.equalsIgnoreCase("Increased Heart Rate"))
        {
            alertLevel = 2;    
        }
        Nurse nurse = system.getNurseDirectory().returnAnOnDutyNurse(system.getNurseDirectory().getNurseList());
        Alert newAlert = system.getAlertsDirectory().createNewAlert(patient, alertReason, alertLevel, reportingProvider, nurse);
        if(newAlert != null)
        {
            if(alertLevel == 1)
            {
                message = "Alert successfully created and status is set to 'Open'! Nurse: " + nurse.getNurseName()+" is on their way"
                        + " for immediate help";
            }
            else if(alertLevel == 2)
            {
                message = "Alert successfully created and status is set to 'Open'!\n\n Ambulance is on the way to "+patient.getPatientAddress()
                        + " for immediate help and your reporting provider Dr. '"+patient.getRegisteredProvider().getProviderName()
                        +"' and nurse '"+nurse.getNurseName()+"'  has been informed.";
            }
            else if(alertLevel == 0)
            {
                message = "Alert successfully created and status is set to 'Open' and sent to your reporting provider Dr. '"+patient.getRegisteredProvider().getProviderName()
                        +"' and nurse '"+nurse.getNurseName()+"\nPlease continue your regular medication.";
            }
            JOptionPane.showMessageDialog(this, message);
            btnAlert.setEnabled(false);
        }
    }//GEN-LAST:event_btnAlertActionPerformed


