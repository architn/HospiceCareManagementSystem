    private void checkifPatientHasAnExistingAlertInSystem() {
        
        if(system.getAlertsDirectory().doesPatientHaveOneOpenAlertAlreadyInSystem(patient, 
                system.getAlertsDirectory().getListOfAlerts()))
        {
            btnAlert.setEnabled(false);
        }
    }
